package com.twobytes.express.form;

public class ShopCustomerGridData {
	private String cuscod;
	private String custyp;
	private String fullName;
	private String fullAddr;
	private String tel;
	private String contact;
	private String remark;
	
	public String getCuscod() {
		return cuscod;
	}
	public void setCuscod(String cuscod) {
		this.cuscod = cuscod;
	}
	public String getFullName() {
		return fullName;
	}
	public void setFullName(String fullName) {
		this.fullName = fullName;
	}
	public String getFullAddr() {
		return fullAddr;
	}
	public void setFullAddr(String fullAddr) {
		this.fullAddr = fullAddr;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getContact() {
		return contact;
	}
	public void setContact(String contact) {
		this.contact = contact;
	}
	public String getCustyp() {
		return custyp;
	}
	public void setCustyp(String custyp) {
		this.custyp = custyp;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}
